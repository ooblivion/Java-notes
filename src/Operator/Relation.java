package Operator;

public class Relation {
    public static void main(String[] args) {
        //关系运算符返回的结果    boolean true or false

        int a = 10;
        int b = 11;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a==b);
        System.out.println(a!=b);

    }
}
